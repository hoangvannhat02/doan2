function showmenu(){
    document.querySelector('.item-menu-bar').style.transform = 'translateX(0%)';
    document.querySelector('.item-menu-bar').style.opacity = '1';
}
function hidemenu(){
    document.querySelector('.item-menu-bar').style.transform = 'translateX(-100%)';
    document.querySelector('.item-menu-bar').style.opacity = '0';
}
